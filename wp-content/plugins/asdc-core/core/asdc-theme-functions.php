// Let's add the menus
function asdc_register_menus() {
    $menus = set_asdc_menus(); // todo ??????????????????????????????????????????????????????
    foreach($menus as $id => $args) {
        register_nav_menu($id, $args['title']);
    }
}
add_action('init', 'asdc_register_menus');

// Registering the image sizes
function asdc_register_image_sizes() {
    $sizes = set_asdc_image_size(); // todo ??????????????????????????????????????????????????????
    foreach($sizes as $id => $args) {
        add_image_size($id, $args['w'], $args['h'], $args['c']);
    }
}
add_action('init', 'asdc_register_image_sizes');

// We can also show the image sizes in the editor
function asdc_show_image_sizes($editor_sizes) {
    $sizes = set_asdc_image_size(); // todo ??????????????????????????????????????????????????????
    foreach($sizes as $id => $args) {
        if($args['t'] != '') {
            $editor_sizes[$id] = $args['t'].'['.$args['w'].'x'.$args['h'].']';
        }
    }
    return $editor_sizes;
}
add_filter('image_size_names_choose', 'asdc_show_image_sizes');